// ------------------------------------ 头文件包含 ------------------------------------
// stdio.h：提供 printf、fgets、fflush 等标准输入输出函数
#include <stdio.h>
// stdlib.h：提供 malloc（动态内存分配）和 free（释放内存）函数
#include <stdlib.h>
// string.h：提供 strlen、strcpy 等字符串处理函数
#include <string.h>
// windows.h：提供 SetConsoleOutputCP 函数，用于解决 Windows 控制台中文乱码问题
#include <windows.h>

/**
 * 暂时可以不看
 * ----------------------------------------------------------------------------
 * 1. 函数声明（Function Declaration / Prototype）
 *    作用：告诉编译器，这个函数是存在的，它的参数是什么类型，返回值是什么类型。
 *    注意：这里只写函数头（参数和返回值），不写函数体（大括号里面的内容）。
 *    位置：通常放在 main 函数之前，这样编译到 main 里面的调用时，编译器就知道该怎么处理了。
 * ----------------------------------------------------------------------------
 * 函数名：   get_string
 * 参数：     const char* prompt  -> 提示字符串（不会修改原字符串，所以加 const）
 * 返回值：   char*               -> 指向动态分配内存的指针（存放用户输入），失败返回 NULL
 * 功能：     打印提示并在末尾加冒号，读取用户输入的一整行，去除换行符后返回
 */
char* get_string(const char* prompt);

// 可以修改内容
// ------------------------------------ 主函数入口 ------------------------------------
int main() {
    // 第1步：解决 Windows 控制台中文乱码问题
    // 将控制台的输出代码页设置为 UTF-8（65001），这样 printf 打印的中文就不会显示成乱码了
    // 注意：这句代码只在 Windows 系统下有效，且需要包含 <windows.h>
    SetConsoleOutputCP(CP_UTF8);

    // 第2步：定义一个字符指针，用来接收 get_string 返回的地址
    char* answer = NULL;

    // 第3步：调用我们声明的 get_string 函数，传入提示语 "What's your name"
    // 你可以使用你喜欢的提示语
    // 函数会在控制台显示 "What's your name? :"，并等待我们输入
    answer = get_string("What's your name? ");

    // 第4步：判断函数是否执行成功
    // 如果返回的是 NULL，说明读取失败（例如用户按了 Ctrl+Z 或者发生错误）
    if (answer == NULL) {
        printf("错误：读取输入失败！\n");
        return 1;  // 返回非 0 值表示程序异常退出
    }

    // 如果想打印 answer 该如何做
    printf("hello, answer\n");
    printf("hello, %s\n", answer);

    // 第5步：打印用户输入的内容（这里故意用中文输出，验证编码是否正确）
    printf("您输入的是：%s\n", answer);

    // 第6步：释放动态分配的内存！！！
    // 因为 get_string 内部使用了 malloc 申请了堆内存，如果不释放，会造成内存泄漏（Memory Leak）
    free(answer);

    // 第7步：程序正常结束，返回 0
    return 0;
}

// 下方内容暂时可以不看
// ============================================================================
// 2. 函数定义（Function Definition）
//    位置：放在 main 函数的后面（或者放在任意 .c 文件里都可以）
//    这里才是真正实现函数逻辑的地方，包含具体的大括号代码块。
// ============================================================================

/**
 * 函数实现：get_string
 * 
 * 详细执行步骤：
 * 1. 打印提示字符串并在末尾加冒号（例如 "What's your name:"），光标不换行。
 * 2. 强制刷新输出缓冲区（fflush），确保提示立刻显示在屏幕上。
 * 3. 使用 fgets 读取用户输入的一整行（包含空格），最大长度为 1023 个字符。
 * 4. 找到字符串末尾的换行符 '\n' 并将其替换为 '\0'（字符串结束符），去掉多余换行。
 * 5. 在堆（Heap）上动态申请一块刚好能容纳该字符串的内存（malloc）。
 * 6. 将缓冲区的数据复制到新申请的内存中。
 * 7. 返回这块内存的首地址。
 */
char* get_string(const char* prompt) {
    // ----------------------- A. 输出提示信息 -----------------------
    // 使用 printf 打印提示，格式为 "提示内容:" （不加换行，让用户直接在冒号后面输入）
    printf("%s:", prompt);
    
    // fflush(stdout) 的作用：强制将输出缓冲区的内容立即显示到屏幕上。
    // 因为某些系统（如 Windows）可能不会立即刷新，加上这句可以保证用户能看到提示。
    fflush(stdout);

    // ----------------------- B. 读取用户输入 -----------------------
    // 定义一个局部字符数组（缓冲区），大小 1024 字节，用于暂存用户输入
    // 这个数组在栈（Stack）上，函数结束后会自动销毁，所以不能直接返回它的地址。
    char buffer[1024];

    // fgets 函数：从标准输入（stdin）读取一行字符串
    // 参数1：存放读取内容的缓冲区 buffer
    // 参数2：缓冲区大小 sizeof(buffer)，最多读取 1023 个字符（留一个给 '\0'）
    // 参数3：从哪里读，stdin 表示标准输入（键盘）
    // 返回值：如果读取成功返回 buffer 的地址，如果遇到文件结束符（EOF）或错误则返回 NULL
    if (fgets(buffer, sizeof(buffer), stdin) == NULL) {
        // 如果用户按了 Ctrl+Z 或者发生错误，返回 NULL 给调用者
        return NULL;
    }

    // ----------------------- C. 处理字符串末尾的换行符 -----------------------
    // strlen 获取当前字符串的长度（不包括末尾的 '\0'）
    size_t len = strlen(buffer);

    // 检查条件：字符串长度大于 0，并且最后一个字符是换行符 '\n'
    if (len > 0 && buffer[len - 1] == '\n') {
        // 将换行符替换为字符串结束符 '\0'，相当于去掉了换行
        buffer[len - 1] = '\0';
    }
    // 注意：如果用户输入的字符超过 1023 个，fgets 不会读取多余字符，
    // 缓冲区末尾可能没有换行符，这种情况我们简单处理，不额外扩展缓冲区。

    // ----------------------- D. 动态分配内存并复制数据 -----------------------
    // strlen(buffer) + 1 是为了给末尾的 '\0' 留出空间
    // malloc 在堆上申请内存，这块内存不会随函数结束而销毁，所以可以返回给 main 使用
    char* result = (char*)malloc(strlen(buffer) + 1);

    // 检查 malloc 是否成功（如果内存不足，malloc 会返回 NULL）
    if (result == NULL) {
        // 如果内存申请失败，返回 NULL 给调用者
        return NULL;
    }

    // strcpy 将缓冲区 buffer 的内容（已经去掉换行符）复制到新申请的 result 内存中
    strcpy(result, buffer);

    // 返回指向堆内存的指针
    return result;
}